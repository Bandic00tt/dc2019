    <p>В <strong>валковые дробилки</strong> материал затягивается силами трения и раздавливается между двумя параллельными цилиндрическими валками, вращающимися с одинаковой скоростью навстречу друг другу. Валки затягивают кусок материала, если диаметр валка приблизительно в 20 раз больше размера куска.</p>

    <h3>Молотковые дробилки</h3>

    <p><strong>Молотковые дробилки</strong> дробят материал ударами молотков, шарнирно закрепленных на быстро вращающемся роторе (окружная скорость на конце молотка до 55м/сек), куски разрушаются также при ударах о плиты корпуса дробилки.</p>
