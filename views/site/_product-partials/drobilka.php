    <p><strong>Дробилка роторная</strong> — транспортирующее устройство для перемещения сыпучих грузов(щебень, песок) непрерывного действия. Рабочим органом является лента.</p>

    <p><strong>Дробилка роторная</strong> - машина, в которой куски породы раздавливаются за счет ударов их о била, установленные на вращающемся роторе. Ударом била куски дробятся и отбрасываются на отбойные плиты. Куски материала, равные или меньше выходной щели, проходят через нее, а не прошедшие куски попадают на била ротора и дробятся до нужного размера.</p>

    <p>Роторные дробилки применяются для первичного (крупного) и вторичного (мелкого) дробления.</p>

    <p>Роторные дробилки просты по конструкции и удобны в техобслуживании. С помощью роторных дробилок можно получать щебень с повышенной кубовидностью.</p>

    <p>Роторные дробилки применяются для дробления горных пород (известняк, мрамор, доломит, мергель, гипс и т.д.) с пределом прочности на сжатие до 100 Мпа.</p>

