    <h3>Дробилка щековая</h3>

    <p><strong>Дробилка щековая</strong> — машина, в которой куски породы периодически раздавливаются между неподвижной и качающейся щеками или обеими подвижными щеками.</p>

    <p><strong>Дробилки щековые</strong> применяются для первой стадии дробления и реже для второй стадии. По конструкции дробилки бывают с простым движением одной подвижной щеки, со сложным движением одной щеки, со сложным движением обеих щек.</p>

    <p>Щековые дробилки применяются для дробления горных пород (гранит, мрамор, диабаз, долонит, базальт, габбро и т.д.) с пределом прочности на сжатие до 300 Мпа.</p>

