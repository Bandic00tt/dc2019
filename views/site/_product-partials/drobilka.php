    <p><strong>Дробилка конусная</strong> — транспортирующее устройство для перемещения сыпучих грузов(щебень, песок) непрерывного действия. Рабочим органом является лента.</p>

    <p><strong>Дробилка конусная</strong> - машина, в которой куски породы раздавливаются за счет изменения зазора между подвижным и неподвижным конусами. Конусные дробилки применяются для вторичной стадии дробления. Подразделяются на дробилки среднего (КСД) и мелкого (КМД) дробления.</p>

    <p>Основные узлы конусных дробилок:</p>

    <ol start="1"><li>Корпус литой</li>
        <li>Корпус-«гайка» с неподвижным конусом-броней</li>
        <li>Вал эксцентриковый с подвижным конусом-броней.</li>
        <li>Группа бронзовых втулок для вращения эксцентрикового вала.</li>
        <li>Вал ведущий со шкивом.</li>
        <li>Коническая передача между ведущим и эксцентриковым валами.</li>
    </ol><p>Конусные дробилки применяются для дробления горных пород (гранит, мрамор, диабаз, долонит, базальт, габбро и т.д.) с пределом прочности на сжатие до 300 Мпа.</p>

