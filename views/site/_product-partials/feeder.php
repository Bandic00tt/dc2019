<p>В последние годы получили широкое распространение питатели вибрационные. В качестве приводного органа применяются вибраторы с регулируемой частотой колебаний. Транспортируемый материал попадает в бункер, который располагается на резиновых или пружинных опорах и далее транспортируется вдоль агрегата за счет колебаний корпуса. Одновременно происходит отсеивание мелкой фракции в колосниках.</p>

<p>Питатели предназначены для равномерной подачи сыпучих и кусковых материалов. Наличие бункеров позволяет загружать питатели с помощью самосвалов и погрузчиков.</p>

<p>Питатель является важным составляющим элементом дробильно-сортировочных установок.</p>

