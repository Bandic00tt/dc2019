<h3>Питатели пластинчатые</h3>

<p><strong>Питатели пластинчатые</strong> — это транспортёры, состоящие из металлических звеньев. Они нужны для равномерной выдачи сыпучих материалов из бункеров, воронок в рабочие машины или транспортирующие механизмы. Работают, как правило, в составе дробильно-сортировочной линии на стадии первичного дробления для получения фракционного щебня.</p>

<p>Транспортирующим устройством является полотно, представляет собой замкнутую цепочку, состоящую из высокопрочных пластин соединенных между собой при помощи шарниров. Производительность регулируется за счет изменения скорости движения транспортирующего полотна. Скорость подачи исходного материала регулируется изменением частоты вращения привода.</p>

<p>После включения привода транспортирующего полотна исходный продукт засыпается в расходный бункер пластинчатого питателя и перемещается к следующему технологическому оборудованию.</p>

<p>Преимущества питателей пластинчатых:</p>

<ul><li>Простота конструкции и технического обслуживания</li>
    <li>Удобство при монтаже</li>
    <li>Высокая надежность </li>
    <li>Долговечность в эксплуатации</li>
